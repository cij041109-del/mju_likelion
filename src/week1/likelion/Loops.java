package week1.likelion;

public class Loops {

    public static void main(String[] args) {


        for(int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        //while

        int b = 10;
        int c= 10;

        while(b > 0) {
            System.out.println(b);
            b--;
        }

        //do-while문

        do {
            System.out.println(c);
            c--;
        }while(c > 0);
    }
}
